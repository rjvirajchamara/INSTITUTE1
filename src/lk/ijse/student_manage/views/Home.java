          private void ShortKeys() {
              KeyboardFocusManager k = DefaultKeyboardFocusManager.getCurrentKeyboardFocusManager();
        k.addKeyEventPostProcessor(new KeyEventPostProcessor() {
            @Override
            public boolean postProcessKeyEvent(KeyEvent e) {
                System.out.println(e.getKeyCode());
                switch (e.getKeyCode()) {

                    case KeyEvent.VK_F1:
                        if (e.getID() == KeyEvent.KEY_PRESSED) {
                            Registration registration = new Registration();
                            registration.setVisible(true);
                            dispose();
                        }
                        break;
                    case KeyEvent.VK_F2:
                        if (e.getID() == KeyEvent.KEY_PRESSED) {
                            peyment payments = new peyment();
                            payments.setVisible(true);
                            dispose();
                        }
                        break;
                    case KeyEvent.VK_F3:
                        if (e.getID() == KeyEvent.KEY_PRESSED) {
                            batch manageCourse = new batch();
                            manageCourse.setVisible(true);
                            dispose();
                        }
                        break;
                    case KeyEvent.VK_F4:
                        if (e.getID() == KeyEvent.KEY_PRESSED) {
                            studentview manageBatch = new studentview();
                            manageBatch.setVisible(true);
                            dispose();
                        }
                        break;
                    case KeyEvent.VK_F5:
                        if (e.getID() == KeyEvent.KEY_PRESSED) {
                            Course checkPaymentStates = new Course();
                            checkPaymentStates.setVisible(true);
                            dispose();
                        }
                        break;
                    case KeyEvent.VK_F6:
                        if (e.getID() == KeyEvent.KEY_PRESSED) {
                            studentview studentDetails = new studentview();
                            studentDetails.setVisible(true);
                            dispose();
                        }
                        break;
                    case KeyEvent.VK_F7:
                        if (e.getID() == KeyEvent.KEY_PRESSED) {
                            BatchTransfer batchTransfer = new BatchTransfer();
                            batchTransfer.setVisible(true);
                            dispose();
                        }
                        break;
                }
                return true;
            }
        });
        this.dispose();
    }




